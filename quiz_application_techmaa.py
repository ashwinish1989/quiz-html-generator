# Certificate Generator

def generate_certificate(name, score_percent, remark):
    file_name = f"certificate_{name}.pdf"
    c = canvas.Canvas(file_name, pagesize=landscape(A4))
    width, height = landscape(A4)

    # üñºÔ∏è Border
    c.setStrokeColor(colors.black)
    c.setLineWidth(4)
    c.rect(0.5*inch, 0.5*inch, width - inch, height - inch)

    # üñºÔ∏è Logo
    try:
        c.drawImage("certified_PNG24.png", width/2 - 1*inch, height - 2*inch, width=2*inch, height=1.5*inch, preserveAspectRatio=True)
    except:
        print("‚ö†Ô∏è Logo not found ‚Äî upload 'certified_PNG24.png' before generating the certificate.")

    # üèÜ Title
    c.setFont("Helvetica-Bold", 28)
    c.drawCentredString(width/2, height - 2.8*inch, "Certificate of Achievement")

    # ‚ú® Subtitle
    c.setFont("Helvetica", 16)
    c.drawCentredString(width/2, height - 3.6*inch, "This is to certify that")

    # üë§ Name
    c.setFont("Helvetica-Bold", 22)
    c.drawCentredString(width/2, height - 4.5*inch, name)

    # üß† Quiz result
    c.setFont("Helvetica", 16)
    c.drawCentredString(width/2, height - 5.5*inch,
                        f"has successfully completed the quiz with a score of {score_percent:.2f}%")

    # üí¨ Remark
    c.setFont("Helvetica-Oblique", 16)
    c.drawCentredString(width/2, height - 6.3*inch, f"Remark: {remark}")

    # ‚úçÔ∏è Signature and Date lines
    c.line(width/2 - 3*inch, height - 7*inch, width/2 - 1*inch, height - 7*inch)
    c.line(width/2 + 1*inch, height - 7*inch, width/2 + 3*inch, height - 7*inch)

    c.setFont("Helvetica", 12)
    c.drawString(width/2 - 2.8*inch, height - 7.3*inch, "Authorized Signature")
    c.drawString(width/2 + 1.2*inch, height - 7.3*inch, "Date")

    # üïì Date
    c.setFont("Helvetica", 12)
    c.drawString(width/2 + 1.2*inch, height - 7.5*inch, datetime.now().strftime("%Y-%m-%d"))

    # üî¢ Certificate ID
    cert_id = f"CERT-{random.randint(10000,99999)}"
    c.setFont("Helvetica-Oblique", 10)
    c.drawRightString(width - inch, 0.7*inch, f"Certificate ID: {cert_id}")

    c.save()
    print(f"üèÖ Certificate saved as: {file_name}")

generate_certificate("Ashwini", 95, "Excellent")